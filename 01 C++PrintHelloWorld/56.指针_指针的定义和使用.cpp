#include <iostream>
using namespace std;
#include <string>
int main56()
{
	//定义指针
	int a = 10;
	//指针定义的语法： 数据类型 * 指针变量名；
	int * p;//一般用p
	//让指针记录变量a的地址
	p = &a;//&是取址符 此处用来获取a的地址
	cout << "a的地址为：" << &a << endl;
	cout << "指针p为：" << p << endl;
	//使用指针
	//可以通过解引用的方式来找到指针指向的内存
	//指针前加一个 * 代表解引用 找到指针指向的内存中的数据

	*p = 1000;
	cout << "a =" << a << endl;
	cout << "*p =" << *p << endl;



	//在32位操作系统下 指针占用四个字节空间
	//在64位操作系统下 指针占用八个字节空间

	system("pause");

	return 0;



}